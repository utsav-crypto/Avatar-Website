            <div id="event_abstract" style="display: none;">
                <div>
                    <h3>Objective:</h3>
                    <p style="margin-left:30px;">Team have to build an autonomous robot which can follow black lines on a white background and 
                       reach from starting line to finishing line as soon as possible. Robot must be able to detect 
                       particular line and keep following it.
                    </p>
                    <h3>Robot Specifications:</h3>
                    <ol>
                        <li>The following size limitations apply for each robot <br> 
                            Width – 200mm max<br>
                            Length – 200mm max<br>
                            Height – no limit<br>
                        </li>
                        <li>The robot cannot have potential more than 12V between any two points.</li>
                        <li>The robot must be controlled autonomously with no human aid.</li>
                        <li>The robot must be started manually with a start button.</li>
                        <li>The robot can be powered by a power source such as a battery fixed on the robot.</li>
                        <li>Participants should keep spare batteries otherwise; it may lead to disqualification if bot is not ready or stops in trial.</li>
                    </ol>
                    <h3>Rules and Regulations:</h3>
                    <ol>
                        <li>A team can have maximum of 4 members.</li>
                        <li>Between trials, participants may not feed information about the arena to the machine. However, participants are allowed to: Adjust sensors (Gain, Position etc.), Change speed   settings and Make repairs.</li>
                        <li>However, a participant may not alter a machine in a manner that alters its weight(e.g. removal of a bulky sensor array or switching to lighter batteries to get better speed).The judges hall arbitrate.</li>
                        <li>One team member is elected as the robot handler. Only that team member is permitted to handle the robot during the game. All other team members must remain outside the game zone.</li>
                        <li>Participants who misbehave may be asked to leave the competition area and risk being disqualified from the contest.</li>
                        <li>Robots or participants that cause deliberate interference with other robots or damage to the    arena will be disqualified.</li>
                        <li>All decisions about scoring, game play and timing made by the Organizing Committee are final.Teams should completely respect their vote and decisions.</li>
                    </ol>
