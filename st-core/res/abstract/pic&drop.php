            <div id="event_abstract" style="display: none;">
                <div>
                    <p>Teams have to build a manually controlled bot which can do simple tasks of gripping blocks and puttingthem in target zones so that it can complete the route by overcoming the Hurdles in its path. The bot can be wired or wireless. </p>
                    <h3>ARENA:</h3>
                    <ul>
                        <li>The outer dimensions of the arena are 3000mm X 2500 mm (lxb).</li>
                        <li>Four thermocol blocks with each of dimensions 150mm X 150mm X 100mm (lxbxh).</li>
                    </ul>
                    <h3>ROBOT SPECIFICATIONS:</h3>
                    <ul>
                        <li>The maximum dimension of the robot can be 30x 25cm (l x b).</li>
                        <li>The robot may be wired or wireless</li>
                        <li>The length of the wire (for wired bots) should be long enough to cover the whole track and wire should remain slack during the complete run.</li>
                        <li>Max weight should be limited by 5kg</li>
                        <li>Readymade toys car are not allowed.</li>
                    </ul>
                    <h3>BATTERIES AND POWER:</h3>
                    <ul>
                        <li>The ROBO must be powered electrically only, use of IC engine is not allowed.</li>
                        <li>Battery voltage: Maximum allowed is 12V & 2A.</li>
                        <li>Participants cannot bring their own power supply units.</li>
                    </ul>
                    <h3>GAME RULES:</h3>
                    <li>The bot would be checked for safety before starting and will be disqualified if found unsafe for other participants.</li>
                    <li>Only one team member is allowed to handle the bot. No other team member is allowed to enter the arena.</li>
                    <li>The bot will be liable for disqualification if it causes any kind of damage to the arena.</li>
                    <li>The bot is <b>not allowed to slide the blocks</b> against the ground except for fine adjustments in the deposit Zone.</li>
                    <li>Any damage done to the blocks will lead to immediate disqualification.</li>
                    <li><b>Maximum of 6 minutes will be given for each team .</b></li>
                    <li>The arena has 4 checkpoints, In case the bot gets stuck at any place, then the block it is carrying (if any) will be repositioned at its initial position and the bot will be kept in the checkpoints corresponding tothat zone. There will be no penalty for this.</li>
                    <li>If you bring <b>wireless robot then we will awarded extra points.</b></li>
                    <li>The blocks which are correctly deposited in deposit zones/pushed from ramp won’t be disturbed.</li>
                    <li>The timer won’t be stopped during this process.</li>
                    <li>In case of any disputes/discrepancies, the organizer’s decision will be final and binding .</li>
                    <li>The organizer’s reserves the right to change any or all of the above rules as they deem fit .</li>
                    <li>Change in rules, if any will be highlighted on the website and notified to the registered teams.</li>
                    <h3></h3>
                    <h3>JUDGING:</h3>
                    <ul>
                        <li>30 points will be awarded for successfully crossing hurdles between the START mark Checkpoint A.</li>
                        <li>20 points will be awarded for placing the Block 1 in the Deposit Zone 1.</li>
                        <li>20 points will be awarded for placing the Block 2 in the Deposit Zone 2 and crossing the wedge, points will be awarded only once for crossing the wedge. Points will not be awarded if the bot crosses the wedge multiple times.</li>
                        <li>20 points will be awarded for placing the Block 3 in the Deposit Zone 3.</li>
                        <li>10 points will be awarded for crossing the Checkpoint D.</li>
                        <li>30 points will be awarded to Land safely in the Sand Box while holding the Block 4.</li>
                        <li>20 points will be awarded to keep the Block 4 in the Deposit Zone 4.</li>
                        <li>60 extra points for crossing the bridge.</li>
                    </ul>
                    <h3>TEAM SPECIFICATIONS:</h3>
                    <ul>
                        <li>A team may consist of a maximum of 4 members.</li>
                        <li>All members should be from same college.</li>
                    </ul>
