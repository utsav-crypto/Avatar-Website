            <div id="event_abstract" style="display: none;">
                <div>
                    <h3>EVENT DESCRIPTION:</h3>
                    <p>Robowars is an event wherein manually controlled RC bots battle each other. The robot that immobilizes the opponent robot is declared victorious.</p>
                    <h3>FORMAT:</h3>
                    <ol>
                        <li>The first round will be an abstract submission round.</li>
                        <li>The written abstract should be prepared on the following lines:</li>
                        <ul style="list-style-type: disc;">
                            <li>The weapon systems and power supply method should be explained in detail, along with proper diagrams. Picture(s) showing these should be attached</li>
                            <li>Description of any unusual advantageous mechanism used.</li>
                            <li>The specifications of all the components used, including motors, suspension springs, remote controller, wires, battery etc. have to be mentioned.</li>
                            <li>Abstract file name should be named as Team name and College name</li>
                            <li>The abstract can be a .pdf file or a word file only.</li>
                        </ul>
                        <li>The teams will be divided into groups and random fixtures will be allotted inside the group such that each team will play knockout matches within the group.</li>
                        <li>Top 2 teams in each group will be selected for quarterfinals and will have one on one knockout matches. </li>
                        <li>4 teams will be selected for semifinals out of which 2 will be selected based on one on one matches. </li>
                    </ol>
                    <h3>DIMENSIONS AND FABRICATIONS:</h3>
                    <ol>
                        <li>The machine should fit in a box of dimension 300 mm * 300 mm * 200 mm (l * b * h) at any given point during the match. The external device used to control the machine or any external tank is not included in the size constraint.</li>
                        <li>The machine should not exceed 20 kg of weight including the weight of onboard circuits, power source, wheels and weapons.</li>
                        <li>Weight of adaptors and the remote controller will not be counted.</li>
                    </ol>
                    <h2>RULES:</h2>
                    <h3>Team Specification:</h3>
                    <ul>
                        <li>Maximum of 5 members in a team. </li>
                        <li>All students must have institute identity card.</li>
                    </ul>
                    <h3>Safety Rules:</h3>
                    <ul>
                        <li>Compliance with all event rules is mandatory. It is expected that competitors stay within the rules and procedures of their own accord and do not require constant policing.</li>
                        <li>Special care should be taken to protect the on-board batteries, robot without proper protection will not be allowed to compete. </li>
                        <li>If you have a robot or weapon design that does not fit within the categories set forth in these rules or is in some way ambiguous or borderline, please contact the event organizers. Safe innovation is always encouraged, but surprising the organizers with your brilliant exploitation of a loophole may cause your robot to be disqualified before it even competes</li>
                        <li>Each event has safety inspections. It is at their sole discretion that your robot is allowed to compete. As a builder you are obligated to disclose all operating principles and potential dangers to the inspection staff. </li>
                        <li>Proper activation and deactivation of robots is critical. Robots must only be activated in the arena, testing areas, or with expressed consent of the event coordinators.</li>
                        <li>All participants build and operate robots at their own risk. Combat robotics is inherently dangerous. There is no amount of regulation that can encompass all the dangers involved. Please take care to not hurt yourself or others when building, testing and competing. </li>
                        <li>All weapons must have a safety cover on any sharp edges to the surroundings during the stay of the teams in the competition area should not be carried out without the consent of organizers. Not following this rule may result in disqualification.</li>
                        <li>All the resources provided at the time of competition from the organizers should be strictly used only after the consent of the organizers. </li>
                        <li>Once the robots have entered into the arena, no team members can enter into the arena at any point of time. In case if fight has to be halted in between and some changes have to be done in the arena or condition of the robot(s), it will be done by organizers only.</li>
                    </ul>
                    <h3>Mobility:</h3>
                    <ul>
                        <li>All robots must have easily visible and controlled mobility in order to compete. </li>
                        <li>Any machine component should not be detached (intentionally) during any point of the war. </li>
                        <li>Methods of Mobility include: </li>
                        <ul>
                            <li>Rolling (wheels, tracks or the whole robot)</li>
                        </ul>
                        <li>Not allowed: <li>
                            <ul>
                                <li>Jumping and hopping. </li>
                                <li>Flying (airfoil using, helium balloons, ornithopters, hovercrafts etc.)</li>
                            </ul>
                    </ul>
                    <h3>Robot Control Requirements:</h3>
                    <ul>
                        <li>The machine can be controlled through wireless and wired remote. The wire must be minimum 8 meter long.</li>
                        <li>The machines using wireless remote must at least have a four frequency remote control circuit or two dual control circuits which may be interchanged before the start of the race to avoid frequency interference with other teams. The case of any interference in the wireless systems will not be considered for rematch or results. </li>
                        <li>Remote control systems from toys might be used. Remote control systems available in the market may also be used.</li>
                    </ul>
                    <h3>Battery and Power:</h3>
                    <ul>
                    <li>The machine can be powered electrically only. Use of an IC engine in any form is not allowed. On board Batteries must be sealed, immobilized-electrolyte types (such as gel cells, lithium, NiCad, NiMH, or dry cells). </li>
                    <li>The electric voltage between 2 points anywhere in the machine should not be more than 36 V DC at any point of time. </li>
                    <li>All efforts must be made to protect battery terminals from a direct short and causing a battery fire, failure to do so will cause direct disqualification.</li>
                    <li>Use of damaged, non-leak proof batteries may lead to disqualification. </li>
                    <li>Special care should be taken to protect the on-board batteries. </li>
                    <li>Change of battery will not be allowed during the match. </li>
                    <li>One 220V power socket will be provided for each team.</li>
                </ul>
                <h3>Weapons Systems:</h3>
                <ul>
                    <li>Robots can have any kind of magnetic weapons, cutters, flippers, saws, lifting devices, spinning hammers etc. as weapons with following exceptions and limitations:</li>
                    <ol>
                        <li>Liquid projectiles. </li>
                        <li>Any kind of inflammable liquid</li>
                        <li>Flame-based weapons. </li>
                        <li>Any kind of explosive or intentionally ignited solid or potentially ignitable solid. </li>
                        <li>Nets, tape, glue, or any other entanglement device.</li>
                        <li>High power magnets or electromagnets. </li>
                        <li>Radio jamming, tasers, tesla coils, or any other high-voltage device. </li>
                        <li>Tethered or un-tethered projectiles. </li>
                    </ol>
                    <li>Spinning weapons which do not come in contact with the arena at any point of time are allowed. </li>
                    <li>In no case should the arena be damaged by any bot.</li>
                </ul>
                <h3>JUDGING CRITERIA:</h3>
                <ul>
                    <li>A robot is declared victorious if its opponent is immobilized i.e. a robot will be declared immobilized if it cannot display any movement for a time period of 20 seconds. </li>
                    <li>In case both the robots remain mobile after the end of the round then the winner will be decided subjectively based on the score calculated. </li>
                    <li>Base score is calculated based on three parameters: Aggression, Control and Damage.</li>
                    <li>Extra points will be awarded based on using the arena and its specification constraints. For Eg: If the arena has an area with sand, extra points will be rewarded to the bot if it pushes the enemy bot into that area. </li>
                    <li>Detailed Score criteria will be declared after declaring the arena.</li>
                </ul>
